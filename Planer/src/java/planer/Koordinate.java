package planer;

/**
 *
 * @author PC
 */
public class Koordinate {
    
    double k1;
    double k2;

    public Koordinate(double k1, double k2) {
        this.k1 = k1;
        this.k2 = k2;
    }

    public double getK1() {
        return k1;
    }

    public void setK1(double k1) {
        this.k1 = k1;
    }

    public double getK2() {
        return k2;
    }

    public void setK2(double k2) {
        this.k2 = k2;
    }
    
}
